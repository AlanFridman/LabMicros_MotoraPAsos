/*************************************************************************************************/
void Motor_Speed(void)
{
	ADC_Init();
	Valor_voltaje = Analog_Read();
	calculo_tiempo_us =((unsigned short)(Valor_voltaje))*488+500;
	tiempo_us = (calculo_tiempo_us)/100;
	delay_us_MTIM(tiempo_us);
}
