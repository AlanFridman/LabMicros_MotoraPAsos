/*************************************************************************************************/
/*********************				   Exported Functions					**********************/
/*************************************************************************************************/
void Motor_Speed(void)
{
	Valor_voltaje = ADC_Read();
	calculo_tiempo_us =(u32)((Valor_voltaje)*48824+50000);
	tiempo_us = calculo_tiempo_us/1000;
	delay_us_MTIM(tiempo_us);
}
